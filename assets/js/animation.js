  // Animation state
  let animationTime = 0;
  let animationFrameId = null;
  let isMobile = false;

  // Mobile detection and optimization
  function checkMobile() {
    isMobile = window.innerWidth < 768;
  }

  // Filter paths for mobile (show fewer paths for better performance)
  function getVisiblePaths() {
    if (isMobile) {
      // On mobile, show every 2nd path (16 paths instead of 32)
      return paths.filter((_, index) => index % 2 === 0);
    }
    return paths;
  }

  // Calculate dash offset for each path based on animation time
  function getDashOffset(path) {
    // Reduce speed on mobile for better performance
    const speedMultiplier = isMobile ? 0.7 : 1;
    const speed = (path.speed || 20) * speedMultiplier;
    const delay = path.delay || 0;
    const adjustedTime = Math.max(0, animationTime - delay);
    // Animate the dash offset continuously
    const offset = (path.dashOffset + adjustedTime * speed) % 150;
    return offset;
  }

  // Initialize SVG with gradients and paths
  function initAnimationSVG() {
    const container = document.querySelector(".animation-svg-container");
    if (!container) return;

    const svg = container.querySelector("svg");
    if (!svg) return;

    const defs = svg.querySelector("defs");
    if (!defs) return;

    // Create gradients
    gradients.forEach((gradient) => {
      const linearGradient = document.createElementNS(
        "http://www.w3.org/2000/svg",
        "linearGradient"
      );
      linearGradient.setAttribute("id", `__lottie_element_${gradient.id}`);
      linearGradient.setAttribute("spreadMethod", "pad");
      linearGradient.setAttribute("gradientUnits", "userSpaceOnUse");
      linearGradient.setAttribute("x1", gradient.x1);
      linearGradient.setAttribute("y1", gradient.y1);
      linearGradient.setAttribute("x2", gradient.x2);
      linearGradient.setAttribute("y2", gradient.y2);

      gradientStops.forEach((stop) => {
        const stopElement = document.createElementNS(
          "http://www.w3.org/2000/svg",
          "stop"
        );
        stopElement.setAttribute("offset", stop.offset);
        stopElement.setAttribute("stop-color", stop.color);
        linearGradient.appendChild(stopElement);
      });

      defs.appendChild(linearGradient);
    });

    // Create path container
    const clipPathGroup = svg.querySelector("g[clip-path]");
    if (!clipPathGroup) return;

    // Animation loop
    function animate() {
      animationTime += 0.016; // ~60fps

      // Update paths
      const visiblePaths = getVisiblePaths();
      visiblePaths.forEach((path) => {
        const pathElement = clipPathGroup.querySelector(
          `path[data-path-id="${path.id}"]`
        );
        if (pathElement) {
          const offset = getDashOffset(path);
          pathElement.style.strokeDashoffset = offset;
        }
      });

      animationFrameId = requestAnimationFrame(animate);
    }

    // Create path elements
    function createPaths() {
      // Clear existing paths
      const existingPaths =
        clipPathGroup.querySelectorAll("g[data-path-group]");
      existingPaths.forEach((p) => p.remove());

      const visiblePaths = getVisiblePaths();
      visiblePaths.forEach((path) => {
        const group = document.createElementNS(
          "http://www.w3.org/2000/svg",
          "g"
        );
        group.setAttribute("transform", path.transform);
        group.setAttribute("opacity", path.opacity);
        group.setAttribute("data-path-group", "true");
        group.style.display = "block";

        const innerGroup = document.createElementNS(
          "http://www.w3.org/2000/svg",
          "g"
        );
        innerGroup.setAttribute("opacity", "1");
        innerGroup.setAttribute("transform", "matrix(1,0,0,1,0,0)");

        const pathElement = document.createElementNS(
          "http://www.w3.org/2000/svg",
          "path"
        );
        pathElement.setAttribute(
          "stroke",
          `url(#__lottie_element_${path.gradientId})`
        );
        pathElement.setAttribute("stroke-linecap", path.linecap);
        pathElement.setAttribute("stroke-linejoin", "miter");
        pathElement.setAttribute("fill-opacity", "0");
        pathElement.setAttribute("stroke-miterlimit", "4");
        pathElement.setAttribute("stroke-opacity", "1");
        pathElement.setAttribute("stroke-dasharray", "2 4");
        pathElement.setAttribute("stroke-width", "1");
        pathElement.setAttribute("d", path.d);
        pathElement.setAttribute("data-path-id", path.id);
        pathElement.style.strokeDashoffset = path.dashOffset;

        innerGroup.appendChild(pathElement);
        group.appendChild(innerGroup);
        clipPathGroup.appendChild(group);
      });
    }

    // Initial setup
    createPaths();

    // Handle resize
    let resizeTimeout;
    window.addEventListener("resize", function () {
      clearTimeout(resizeTimeout);
      resizeTimeout = setTimeout(function () {
        checkMobile();
        createPaths();
      }, 250);
    });

    checkMobile();
    animate();
  }

  // Initialize when DOM is ready
  if (document.readyState === "loading") {
    document.addEventListener("DOMContentLoaded", initAnimationSVG);
  } else {
    initAnimationSVG();
  }

  // Cleanup on page unload
  window.addEventListener("beforeunload", function () {
    if (animationFrameId !== null) {
      cancelAnimationFrame(animationFrameId);
    }
  });
